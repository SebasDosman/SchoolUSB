    private void BotonCrearEmpleadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearEmpleadoActionPerformed
        try{
            Empleado empleado = new Empleado();
            empleado.setId(TextIdEmpleado.getText());
            empleado.setRol(ComboRolEmpleado.getSelectedItem().toString());
            empleado.setNombre(TextNombreEmpleado.getText());
            empleado.setEdad(TextEdadEmpleado.getText());
            empleado.setSexo(ComboSexoEmpleado.getSelectedItem().toString());
            empleado.setSalario(TextSalarioEmpleado.getText());

            if (TextIdEmpleado.getText().isEmpty() || TextNombreEmpleado.getText().isEmpty() || TextEdadEmpleado.getText().isEmpty() || TextSalarioEmpleado.getText().isEmpty()) {
                JOptionPane.showMessageDialog(PanelCompleto, "Todos los campos son obligatorios", "Registrar Empleado", JOptionPane.ERROR_MESSAGE);
            }
            
            if (empleadoLogica.validarEmpleadoId(empleado.getId())) {
                JOptionPane.showMessageDialog(PanelCompleto, "El Id debe ser √∫nico e irrepetible", "Registrar Empleado", JOptionPane.ERROR_MESSAGE);
            } else if (Integer.parseInt(empleado.getEdad()) > 18) {
                empleadoLogica.crearEmpleado(empleado);
                BotonLimpiarEmpleadoActionPerformed(evt);
                JOptionPane.showMessageDialog(PanelCompleto, "Se ha registrado el empleado: " + empleado.getNombre(), "Registrar Empleado", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(PanelCompleto, "El empleado debe ser mayor de edad", "Registrar Empleado", JOptionPane.ERROR_MESSAGE);
            }
        }catch(Exception e){
        } 
    }//GEN-LAST:event_BotonCrearEmpleadoActionPerformed

