    private void IniciarSesionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IniciarSesionActionPerformed
        try {
            if (usuarioLogica.validarUsuario(TextUsuario.getText(), TextContrasena.getText())) {
                JOptionPane.showMessageDialog(AbajoPanel, "Ingresaste con exito", "Iniciar Sesión", JOptionPane.INFORMATION_MESSAGE);
                
                this.setVisible(false);
                new InterfazHomePage().setVisible(true);
            } else {
                JOptionPane.showMessageDialog(AbajoPanel, "Usuario y/o contraseña incorrectos", "Iniciar Sesión", JOptionPane.ERROR_MESSAGE);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(AbajoPanel, e.getMessage());
        }
    }//GEN-LAST:event_IniciarSesionActionPerformed

