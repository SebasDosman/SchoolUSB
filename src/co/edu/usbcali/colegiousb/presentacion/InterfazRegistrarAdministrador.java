    private void BotonRegistroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonRegistroActionPerformed
        try{
            //Utilizamos los get y set
            Usuario usuario = new Usuario();
            usuario.setUsuario(TextUsuarioRegistro.getText());
            usuario.setContrasenia(TextContrasenaRegistro.getText());
            usuario.setNombre(TextNombreRegistro.getText());
            usuario.setApellido(TextApellidoRegistro.getText());
            usuario.setEmail(TextEmailRegistro.getText());
            
            usuarioLogica.crearUsuario(usuario);
            
            TextNombreRegistro.setText(null);
            TextApellidoRegistro.setText(null);
            TextUsuarioRegistro.setText(null);
            TextContrasenaRegistro.setText(null);
            TextEmailRegistro.setText(null);
            
            JOptionPane.showMessageDialog(AbajoPanel, "Se ha regristrado correctamente: " + usuario.getNombre(), "Registrar Administrador", JOptionPane.INFORMATION_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(AbajoPanel, e.getMessage(), "Registrar Administrador", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_BotonRegistroActionPerformed

